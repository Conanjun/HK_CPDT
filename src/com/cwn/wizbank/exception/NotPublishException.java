public class NotPublishException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -7766803303792719052L;

	public NotPublishException() {
		super();
	}

	public NotPublishException(String message, Throwable cause) {
		super(message, cause);
	}

	public NotPublishException(String message) {
		super(message);
	}

	public NotPublishException(Throwable cause) {
		super(cause);
	}

	
	
}
